# This class is responsible to hold every color code used to make things prettier.


class ShellColors:
    def __init__(self):
        pass

    GREEN = '\033[92m'
    RED = '\033[91m'
    WARNING = "\033[35m"
    END = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINED = "\033[4m"
    YELLOW = "\033[93m"
    LIGHT_GRAY = "\033[90m"
